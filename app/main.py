# ---------- markdown rendering helper for templates ----------

import markdown as md_lib  # noqa: E402

_md = md_lib.Markdown(extensions=["fenced_code", "tables", "nl2br", "sane_lists"])

# Blog/research posts contain rich raw HTML + inline SVG graphics. Use a
# dedicated instance WITHOUT nl2br (which would inject <br> into multi-line
# markup) and WITH attr_list (so authors can attach classes/ids). Raw HTML
# blocks pass through verbatim — graphics must be wrapped in a block-level
# <div class="pt-..."> (col 0, blank lines around it) to be preserved cleanly.
_md_blog = md_lib.Markdown(extensions=["fenced_code", "tables", "sane_lists", "attr_list"])

# ── Section-header pre-processor for card content ─────────────────────────
# LLM output uses labels like "PRIMARY DRIVER:", "SUPPORTING EVIDENCE:" etc.
# as plain text. Convert them to markdown ### headers so the CSS can style
# them as proper section dividers instead of rendering as a wall of text.

_SECTION_HEADER_RE = re.compile(
    r"^(PRIMARY\s+DRIVER|SUPPORTING\s+EVIDENCE|CATALYST|KEY\s+RISKS?"
    r"|MECHANISM|INVALIDATION(?:\s+CONDITIONS?)?|PROBABILITY\s+DECOMPOSITION"
    r"|CONCLUSION|SUMMARY|KEY\s+ARGUMENTS?|COUNTER[\s-]?ARGUMENTS?"
    r"|RISK\s+FACTORS?|TRADE\s+EXPRESSION|CONTRARIAN\s+ENGAGEMENT"
    r"|NARRATIVE\s+SUMMARY|CONTRADICTIONS(?:\s*/\s*TENSIONS)?)\s*:?\s*$",
    re.IGNORECASE,
)
_SECTION_INLINE_RE = re.compile(
    r"^(PRIMARY\s+DRIVER|SUPPORTING\s+EVIDENCE|CATALYST|KEY\s+RISKS?"
    r"|MECHANISM|INVALIDATION(?:\s+CONDITIONS?)?|CONTRARIAN\s+ENGAGEMENT"
    r"|NARRATIVE\s+SUMMARY|CONTRADICTIONS(?:\s*/\s*TENSIONS)?)\s*:\s*(.+)$",
    re.IGNORECASE,
)


def _format_analysis_sections(text: str) -> str:
    """Convert LLM section labels into proper markdown ### headers."""
    if not text:
        return ""
    lines = text.split("\n")
    out: list[str] = []
    for line in lines:
        s = line.strip()
        m = _SECTION_HEADER_RE.match(s)
        if m:
            out.append("")
            out.append(f"### {m.group(1).strip().title()}")
            out.append("")
            continue
        m2 = _SECTION_INLINE_RE.match(s)
        if m2:
            out.append("")
            out.append(f"### {m2.group(1).strip().title()}")
            out.append("")
            out.append(m2.group(2).strip())
            continue
        out.append(line)
    return "\n".join(out)


def _render_markdown(text: str) -> str:
    if not text:
        return ""
    stripped = text.strip()
    # Defensive: if the whole content is wrapped in a single code fence
    # (LLMs sometimes do this even when not asked), peel it. Otherwise
    # markdown renders the entire brief as one <pre> block that doesn't
    # wrap on mobile.
    if stripped.startswith("```") and stripped.rstrip().endswith("```"):
        lines = stripped.split("\n")
        if len(lines) >= 2 and lines[0].startswith("```") and lines[-1].strip() == "```":
            stripped = "\n".join(lines[1:-1])
    _md.reset()
    return _md.convert(stripped)


def _render_markdown_blog(text: str) -> str:
    """Render a research/blog post: standard markdown + raw HTML/SVG graphics
    preserved verbatim (no nl2br). Used by /research/{slug}."""
    if not text:
        return ""
    stripped = text.strip()
    if stripped.startswith("```") and stripped.rstrip().endswith("```"):
        lines = stripped.split("\n")
        if len(lines) >= 2 and lines[0].startswith("```") and lines[-1].strip() == "```":
            stripped = "\n".join(lines[1:-1])
    _md_blog.reset()
    return _md_blog.convert(stripped)


templates.env.filters["markdown"] = _render_markdown
