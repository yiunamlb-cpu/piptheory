def _build_today_block(run: Run) -> dict:
    """The 'TODAY' panel for the public research site: the top high-
    conviction macro reads. No positions, no chart filter — just the
    Council Judge's verdict per instrument.

    Returns: {"ideas": [...]}. The `actions` key (open-trade alerts in
    the old personal build) is no longer populated; kept as an empty
    list so any older template snippet referencing it still renders.
    """
    candidates: list[dict] = []
    for b in run.instrument_biases:
        co = run.council.get(b.instrument)
        if co and co.judge_conviction:
            conv = co.judge_conviction
            bias_raw = co.judge_bias or b.bias
        else:
            conv = b.conviction
            bias_raw = b.bias
        direction = _normalise_dir_word(bias_raw)
        if direction == "no view" or conv < 6:
            # Public site: surface only moderate-or-better macro reads
            continue

        macro_summary = _extract_macro_summary(b, co).removeprefix("Macro: ").strip()
        candidates.append({
            "kind": "idea",
            "instrument": b.instrument,
            "name": display_label(b.instrument),
            "direction": direction,
            "conviction": conv,
            "strength_band": _strength_band(conv),
            "strength_label": _strength_label(conv),
            "macro_summary": macro_summary,
        })
    candidates.sort(key=lambda c: (-c["conviction"], c["instrument"]))
    ideas = candidates[:4]  # show top 4 max
    return {"actions": [], "ideas": ideas}


